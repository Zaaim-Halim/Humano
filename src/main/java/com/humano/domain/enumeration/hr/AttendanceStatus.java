package com.humano.domain.enumeration.hr;

/**
 * Represents the possible attendance statuses for employee attendance records.
 * Used to track and categorize employee presence and absence.
 */
public enum AttendanceStatus {
    PRESENT,
    ABSENT,
    ON_LEAVE,
    WORK_FROM_HOME,
    LATE,
    HALF_DAY,
    SICK,
    HOLIDAY,
    BUSINESS_TRIP,
    TRAINING,
    UNPAID_LEAVE,
    OVERTIME
}
