package com.humano.domain.enumeration.hr;

/**
 * Represents the type of approval request.
 */
public enum ApprovalType {
    /**
     * Leave request approval.
     */
    LEAVE_REQUEST,

    /**
     * Expense claim approval.
     */
    EXPENSE_CLAIM,

    /**
     * Overtime request approval.
     */
    OVERTIME_REQUEST,

    /**
     * Training enrollment approval.
     */
    TRAINING_REQUEST,

    /**
     * Position transfer approval.
     */
    POSITION_TRANSFER,

    /**
     * Salary adjustment approval.
     */
    SALARY_ADJUSTMENT,

    /**
     * Timesheet approval.
     */
    TIMESHEET_APPROVAL,
}
