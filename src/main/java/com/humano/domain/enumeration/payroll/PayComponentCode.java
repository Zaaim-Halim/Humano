package com.humano.domain.enumeration.payroll;

public enum PayComponentCode {
    /**
     * Basic salary component
     */
    BASIC,
    /**
     * Overtime pay component
     */
    OT,
    /**
     * Personal income tax deduction
     */
    TAX_PIT,
    /**
     * Bonus earning component
     */
    BONUS,
    /**
     * Allowance earning component
     */
    ALLOWANCE,
    /**
     * General deduction component
     */
    DEDUCTION,
    /**
     * Employer charge component (e.g., social contributions)
     */
    EMPLOYER_CHARGE
}
